#include "libft.h"

int	ft_strlen(const char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '%' && str[i + 1])
			j += 2;
		i++;
	}
	return (i - j);
}
